import numpy as np

arr = np.array([2, 4, 6, 8, 10])

print("Original Array:", arr)

# Addition
print("After Adding 5:", arr + 5)

# Subtraction
print("After Subtracting 2:", arr - 2)

# Multiplication
print("After Multiplying by 3:", arr * 3)

# Division
print("After Dividing by 2:", arr / 2)
